         /*=============================================================================*/
         /* OPERATION:   list                                                           */
         /**
          * Returns a list of all the publishers.
          * <p>
          * @return The list of publishers.
          * <p>
          * @since Dec 31, 2014
          */
         /*=============================================================================*/
   public List<Publisher> list ()
   {
      return repository.findAll ();
   }

         /*=============================================================================*/
         /* OPERATION:   createNew                                                      */
         /**
          * Creates and save a new queue instance.
          * <p>
          * @param aName
          *          The name for the publisher.
          *
          * @return the saved instance.
          * <p>
          * @since Dec 31, 2014
          */
         /*=============================================================================*/
   public Publisher createNew (String aName)
   {
      Publisher myPublisher   = new Publisher ();
      myPublisher.setName     (aName);

      return repository.save (myPublisher);
   }

