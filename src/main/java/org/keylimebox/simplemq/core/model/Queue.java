import java.util.ArrayList;
import java.util.List;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

/*======================================================================================*/
/*                           Class Definition / Implementation                          */
/*======================================================================================*/
/*======================================================================================*/
/* CLASS:       Queue                                                                   */
/**
 * Defines a queue that can be published to or subscribed to.
 * <p>
 * @author      etlweather
 * @since       Dec 31, 2014
 */
/*======================================================================================*/
@SuppressWarnings ("nls")
@Document (collection="queues")
public class Queue
{

    /*==================================================================================*/
    /*===================================            ===================================*/
    /*=================================== Attributes ===================================*/
    /*===================================            ===================================*/
    /*==================================================================================*/

    /*==================================================================================*/
    /* Protected Attributes                                                             */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Private Attributes                                                               */
    /*==================================================================================*/



               /*======================================================================*/
                /* ATTRIBUTE: id                                                        */
                /**
                 * The ID of the queue.
                 */
                /*======================================================================*/
   @Id
   private String             id;

                /*======================================================================*/
                /* ATTRIBUTE: name                                                      */
                /**
                 * The name of the queue.
                 */
                /*======================================================================*/
   private String             name;

                /*======================================================================*/
                /* ATTRIBUTE: description                                               */
                /**
                 * The description of the queue.
                 */
                /*======================================================================*/
   private String             description;

                /*======================================================================*/
                /* ATTRIBUTE: subscribers                                               */
                /**
                 * The subscribers to this queue.
                 */
                /*======================================================================*/
   private List<String>       subscribers;

    /*==================================================================================*/
    /* Class Attributes                                                                 */
    /*==================================================================================*/
        /*==============================================================================*/
        /* Constants                                                                    */
        /*==============================================================================*/

        /*==============================================================================*/
        /* Variables                                                                    */
        /*==============================================================================*/

    /*==================================================================================*/
    /*===================================            ===================================*/
    /*=================================== Operations ===================================*/
    /*===================================            ===================================*/
    /*==================================================================================*/

    /*==================================================================================*/
    /* Static initializer                                                               */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Constructors                                                                     */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Attribute Get Operations                                                         */
    /*==================================================================================*/

   public String getId ()
   {
      return (id);
   }

   public String getName ()
   {
      return (name);
   }

   public String getDescription ()
   {
      return (description);
   }

   public List<String> getSubscribers ()
   {
      if (subscribers == null) {
         subscribers = new ArrayList<String> (0);
      }
      return (subscribers);
   }

    /*==================================================================================*/
    /* Attribute Set Operations                                                         */
    /*==================================================================================*/

   public void setId (String aId)
   {
      id = aId;
   }

   public void setName (String aName)
   {
      name = aName;
   }

   public void setDescription (String aDescription)
   {
      description = aDescription;
   }

   public void setSubscribers (List<String> aSubscribers)
   {
      subscribers = aSubscribers;
   }

    /*==================================================================================*/
    /* Private Operations                                                               */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Protected Operations                                                             */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Package Operations                                                               */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Public Operations                                                                */
    /*==================================================================================*/

         /*=============================================================================*/
         /* OPERATION:   addSubscriber                                                  */
         /**
          * Adds a subscriber to the queue.
          * <p>
          * @param aSubscriberId
          * <p>
          * @since Dec 31, 2014
          */
         /*=============================================================================*/
   public void addSubscriber (String aSubscriberId)
   {
      if (subscribers == null) {
         subscribers = new ArrayList<String> ();
      }
      subscribers.add (aSubscriberId);
   }

    /*==================================================================================*/
    /* Abstract Operations (definitions)                                                */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Abstract Operations (implementations)                                            */
    /*==================================================================================*/

    /*==================================================================================*/
    /* Class (static) Operations                                                        */
    /*==================================================================================*/
}

